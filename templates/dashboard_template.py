def _to_float(v) -> float:
    """Parse a numeric value that may arrive as int, float, or a formatted string like '$216,029'."""
    if isinstance(v, (int, float)):
        return float(v)
    if v is None:
        return 0.0
    return float(str(v).replace("$", "").replace(",", "").strip() or 0)


def _fmt_usd(v: float) -> str:
    """Format as USD currency. Uses 4 decimals for sub-penny values, 2 otherwise."""
    if v is None:
        return "—"
    if abs(v) < 0.01:
        return f"${v:,.4f}"
    return f"${v:,.2f}"


def _safe_md(text: str) -> str:
    """Escape dollar signs so Streamlit doesn't render them as MathJax/LaTeX."""
    return text.replace("$", "\\$")

