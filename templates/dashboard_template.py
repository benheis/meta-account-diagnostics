    fig = go.Figure()
    fig.add_bar(
        name="Winners",
        x=labels, y=winners_ct,
        marker_color="#22c55e",
        hovertemplate="<b>%{x}</b><br>Winners: %{y}<extra></extra>",
    )
    fig.add_bar(
        name="Converted (non-winner)",
        x=labels, y=non_winner_ct,
        marker_color="#f59e0b",
        hovertemplate="<b>%{x}</b><br>Converted non-winner: %{y}<extra></extra>",
    )
