RESULTS_PATH = Path.home() / "meta-diagnostics" / "results.json"

VERDICT_COLORS = {
    "HEALTHY":           "#22c55e",
    "WARNING":           "#f59e0b",
    "CRITICAL":          "#ef4444",
    "INSUFFICIENT_DATA": "#6b7280",
}

VERDICT_BG = {
    "HEALTHY":           "#f0fdf4",
    "WARNING":           "#fffbeb",
    "CRITICAL":          "#fef2f2",
    "INSUFFICIENT_DATA": "#f3f4f6",
}


def load_results() -> dict:
    if not RESULTS_PATH.exists():
        st.error(f"Results file not found at {RESULTS_PATH}. Run /meta-diagnostics first.")
        st.stop()
    with open(RESULTS_PATH) as f:
        return json.load(f)


def verdict_badge(verdict: str) -> str:
    color = VERDICT_COLORS.get(verdict, "#6b7280")
    return f'<span style="background:{color};color:white;padding:3px 10px;border-radius:4px;font-weight:700;font-size:13px">{verdict}</span>'


def analysis_card(title: str, verdict: str, meaning: str, action: str, chart_fn):
    bg = VERDICT_BG.get(verdict, "#f9fafb")
    st.markdown(f"""
<div style="border:1px solid #e5e7eb;border-radius:8px;padding:20px;margin-bottom:20px;background:{bg}">
<div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:12px">
<h3 style="margin:0;font-size:16px">{title}</h3>
{verdict_badge(verdict)}
</div>
""", unsafe_allow_html=True)
    chart_fn()
    st.markdown(f"**What it means:** {meaning}")
    st.markdown(f"**Action:** {action}")
    st.markdown("</div>", unsafe_allow_html=True)


