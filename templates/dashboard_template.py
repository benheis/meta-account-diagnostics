def priority_action_stack(analyses: list[dict]):
    criticals  = [(a["title"], a["action"]) for a in analyses if a["verdict"] == "CRITICAL"]
    warnings   = [(a["title"], a["action"]) for a in analyses if a["verdict"] == "WARNING"]
    healthies  = [(a["title"], a["action"]) for a in analyses if a["verdict"] == "HEALTHY"]
    # INSUFFICIENT_DATA analyses are excluded from the priority stack — they're informational notes, not actions

    st.markdown("---")
    st.markdown("## Priority Action Stack")

    rank = 1
    if criticals:
        st.markdown("### 🔴 CRITICAL — do this week")
        for title, action in criticals[:2]:
            st.markdown(f"**{rank}. [{title}]** {action}")
            rank += 1

    if warnings:
        st.markdown("### 🟡 WARNING — do this month")
        for title, action in warnings[:2]:
            st.markdown(f"**{rank}. [{title}]** {action}")
            rank += 1

    if healthies:
        st.markdown("### 🟢 HEALTHY — keep doing")
        for title, action in healthies[:1]:
            st.markdown(f"**{rank}. [{title}]** {action}")


def main():
    st.set_page_config(page_title="Meta Account Diagnostics", layout="wide")

    results = load_results()
    config = results.get("config", {})
    run_date = results.get("run_date", str(date.today()))
    account_id = config.get("account_id", "unknown")
    conv_event = config.get("conversion_event", "conversion")
    conv_label = config.get("conversion_label", conv_event)

    st.title(f"Meta Account Diagnostics — {account_id} — {run_date}")
    st.subheader(f"Optimizing for: {conv_event} ({conv_label})")

    analyses = results.get("analyses", [])
    n_critical = sum(1 for a in analyses if a["verdict"] == "CRITICAL")
    n_warning  = sum(1 for a in analyses if a["verdict"] == "WARNING")
    n_healthy  = sum(1 for a in analyses if a["verdict"] == "HEALTHY")
    n_nodata   = sum(1 for a in analyses if a["verdict"] == "INSUFFICIENT_DATA")

    with st.sidebar:
        st.markdown("### Account Summary")
        overview = results.get("overview", {})
        st.metric("Total Spend (90d)", f"${float(overview.get('spend', 0)):,.0f}")
        st.metric("Active Ads", overview.get("active_ads", "—"))
        st.metric("Date Range", overview.get("date_range", "last_90_days"))
        st.markdown("---")
        st.markdown("### Health Score")
        st.markdown(f"<span style='color:#ef4444;font-weight:700'>🔴 CRITICAL: {n_critical}</span>", unsafe_allow_html=True)
        st.markdown(f"<span style='color:#f59e0b;font-weight:700'>🟡 WARNING: {n_warning}</span>", unsafe_allow_html=True)
        st.markdown(f"<span style='color:#22c55e;font-weight:700'>🟢 HEALTHY: {n_healthy}</span>", unsafe_allow_html=True)
        if n_nodata:
            st.markdown(f"<span style='color:#6b7280;font-weight:700'>⚪ INSUFFICIENT DATA: {n_nodata}</span>", unsafe_allow_html=True)
        st.markdown("---")
        st.caption(f"Run date: {run_date}")

    chart_fns = {
        "Test & Learn Spend Optimization": chart_test_and_learn,
        "Creative Allocation":             chart_creative_allocation,
        "Reliance on Old Creative":        chart_old_creative,
        "Creative Churn":                  chart_creative_churn,
        "Production & Slugging Rate":      chart_slugging_rate,
        "Rolling Reach":                   chart_rolling_reach,
        "Creative Volume vs. Spend":       chart_volume_vs_spend,
    }

    for a in analyses:
        title   = a["title"]
        verdict = a["verdict"]
        meaning = a.get("meaning", "")
        action  = a.get("action", "")
        data    = a.get("data", {})

        def make_chart_fn(t, d):
            fn = chart_fns.get(t)
            return lambda: fn(d) if fn else st.info("Chart not available.")

        analysis_card(title, verdict, meaning, action, make_chart_fn(title, data))

    priority_action_stack(analyses)


if __name__ == "__main__":
    main()
